public class Rueda {

   private int radio;
   private int ancho;

  public Rueda(){
  }
   
    public Rueda(int radio, int ancho) {
        this.radio = radio;
        this.ancho = ancho;
    }

    public int getRadio() {
        return radio;
    }

    public void setRadio(int radio) {
        this.radio = radio;
    }

    public int getAncho() {
        return ancho;
    }

    public void setAncho(int ancho) {
        this.ancho = ancho;
    }
   
    
  
}
